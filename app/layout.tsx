import type { Metadata } from "next";
import { Newsreader, IBM_Plex_Sans, IBM_Plex_Mono } from "next/font/google";
import "./globals.css";

// Light-editorial "record" type: a Newsreader serif carries the display voice
// (a publication/standards register — the one place a serif is justified), a
// neutral Plex Sans reads the body, and Plex Mono sets every figure (tabular).
// Deliberately NOT the Geist/Inter AI-default, and not the banned Fraunces/Playfair.
const newsreader = Newsreader({
  variable: "--font-newsreader",
  subsets: ["latin"],
  weight: ["400", "500", "600"],
  style: ["normal", "italic"],
});

const plexSans = IBM_Plex_Sans({
  variable: "--font-plex-sans",
  subsets: ["latin"],
  weight: ["400", "500", "600"],
});

const plexMono = IBM_Plex_Mono({
  variable: "--font-plex-mono",
  subsets: ["latin"],
  weight: ["400", "500", "600"],
});

export const metadata: Metadata = {
  title: "Tribute: Contributive Attribution for AI answers",
  description:
    "Prove which sources an AI answer actually depended on, by removing each one and regenerating, per answer, tamper-evident. Contributive Attribution, not citation-trust.",
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html
      lang="en"
      className={`${newsreader.variable} ${plexSans.variable} ${plexMono.variable} h-full antialiased`}
    >
      <body className="min-h-full flex flex-col">{children}</body>
    </html>
  );
}
